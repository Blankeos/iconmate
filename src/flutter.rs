// Flutter preset support.
//
// Unlike the JS presets, iconmate fully owns the Flutter barrel file
// (see `_docs/flutter-preset-plan.md` §2.11). This module provides pure
// functions for parsing, rendering, and mutating that barrel, plus a
// lightweight `pubspec.yaml` probe used for Flutter-project detection.

